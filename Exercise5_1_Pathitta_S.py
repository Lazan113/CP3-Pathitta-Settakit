num_a = int(input("Input the first number: "))
num_b = int(input("Input the second number: "))
print("*** Result ***")
print(num_a, "+", num_b, "=", num_a+num_b) #Plus
print(num_a, "-", num_b, "=",num_a-num_b)  #Minus
print(num_a, "*", num_b, "=",num_a*num_b)  #Multiply
print(num_a, "/", num_b, "=",num_a/num_b)  #Divide