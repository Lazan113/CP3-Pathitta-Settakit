def addNumber(x,y):
    print("Plus: ",x+y)
def minusNumber(x,y):
    print("Minus: ", x-y)
def multiplyNumber(x,y):
    print("Multiply: ", x*y)
def divideNumber(x,y):
    print("Divide: ", x/y)

x = int(input("x = "))
y = int(input("y = "))
addNumber(x,y)
minusNumber(x,y)
multiplyNumber(x,y)
divideNumber(x,y)